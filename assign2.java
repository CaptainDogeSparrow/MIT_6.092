class FooCorporation {
    public static void main(String[] arguments){
        double basePay = 9;
        if (basePay < 8.00){
            System.out.println("Base pay is less than $8.00");
        }
  
        int hoursWorked = 35;
        if (hoursWorked > 60){
            System.out.println("Hours worked are greater than 60");
        }
  
        if (hoursWorked > 40){
            System.out.println("The total pay is $" + 1.5*basePay*hoursWorked);
        }
        else{ System.out.println("The total pay is $" + basePay*hoursWorked);}   
    } 
}
